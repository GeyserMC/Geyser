package org.geysermc.geyser.gametest.datagen;

import net.fabricmc.fabric.api.datagen.v1.DataGeneratorEntrypoint;
import net.fabricmc.fabric.api.datagen.v1.FabricDataGenerator;
import net.fabricmc.fabric.api.datagen.v1.FabricPackOutput;
import net.fabricmc.fabric.api.datagen.v1.provider.FabricDynamicRegistryProvider;
import net.minecraft.core.HolderLookup;
import org.geysermc.geyser.gametest.GeyserGameTests;

import java.util.concurrent.CompletableFuture;

public class GeyserGameTestGenerator implements DataGeneratorEntrypoint {

    @Override
    public void onInitializeDataGenerator(FabricDataGenerator generator) {
        FabricDataGenerator.Pack pack = generator.createPack();
        pack.addProvider(DynamicRegistryGenerator::new);
    }

    private static class DynamicRegistryGenerator extends FabricDynamicRegistryProvider {

        private DynamicRegistryGenerator(FabricPackOutput output, CompletableFuture<HolderLookup.Provider> registriesFuture) {
            super(output, registriesFuture);
        }

        @Override
        protected void configure(HolderLookup.Provider registries, Entries entries) {
            GeyserGameTests.bootstrap(registries, entries);
        }

        @Override
        public String getName() {
            return "Geyser Game Test Generator";
        }
    }
}
